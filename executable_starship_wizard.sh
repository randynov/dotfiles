#!/bin/bash

# Starship Prompt Design Wizard
# Interactive configuration builder for starship prompts

clear
echo "ğŸš€ Starship Prompt Design Wizard"
echo "================================="
echo ""
echo "This wizard will help you design a custom starship prompt."
