    echo ""
    echo "ğŸ“‹ Your starship.toml preview:"
    echo "=============================="
    head -30 "$HOME/.config/starship.toml"
    echo ""
    echo "(showing first 30 lines - full config saved to ~/.config/starship.toml)"
fi

echo ""
echo "ğŸ‰ Starship wizard complete! Enjoy your new prompt!"